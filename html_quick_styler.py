# ============================================================================
# HTMLGenerationEngine
# ============================================================================
class HTMLGenerationEngine:
    @staticmethod
    def generate_page(title, description, style="modern", uploaded_images=None):
        palette       = ColorPaletteGenerator.generate_palette(style)
        sections      = HTMLPageBuilder.extract_sections(description)
        meta_tags     = HTMLPageBuilder.generate_meta_tags(title, description)
        css           = HTMLPageBuilder.generate_css(palette)
        sections_html = "\n\n".join([
            HTMLPageBuilder.generate_section_html(s, palette, uploaded_images)
            for s in sections
            if HTMLPageBuilder.generate_section_html(s, palette, uploaded_images)
        ])
        html = f"""<!DOCTYPE html>
<html lang="en">
<head>
{meta_tags}
{css}
</head>
<body>
    <header>
        <nav>
            <h1>{title}</h1>
            <div>
                <a href="#features">Features</a>
                <a href="#about">About</a>
                <a href="#contact">Contact</a>
            </div>
        </nav>
    </header>
    <main>
{sections_html}
    </main>
</body>
</html>"""
        return html, palette


