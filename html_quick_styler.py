class ImageProvider:
    IMAGES = {
        "hero":        ["1029", "1040", "1042", "1043", "1044"],
        "about":       ["1005", "1006", "1008", "1010", "1012"],
        "portfolio":   ["1015", "1016", "1018", "1020", "1021",
                        "1022", "1023", "1024", "1025", "1026"],
        "team":        ["1027", "1028", "1032", "1033", "1035"],
        "testimonial": ["1036", "1038", "1039", "1041", "1045"],
    }

    @staticmethod
    def picsum(context: str, index: int = 0, width: int = 800, height: int = 500) -> str:
        pool   = ImageProvider.IMAGES.get(context, ImageProvider.IMAGES["portfolio"])
        pic_id = pool[index % len(pool)]
        return f"https://picsum.photos/id/{pic_id}/{width}/{height}"

    @staticmethod
    def avatar(index: int = 0, size: int = 120) -> str:
        pool   = ImageProvider.IMAGES["team"]
        pic_id = pool[index % len(pool)]
        return f"https://picsum.photos/id/{pic_id}/{size}/{size}"

    @staticmethod
    def file_to_data_url(filepath: str) -> str:
        try:
            mime, _ = mimetypes.guess_type(filepath)
            if not mime or not mime.startswith("image/"):
                mime = "image/jpeg"
            with open(filepath, "rb") as f:
                b64 = base64.b64encode(f.read()).decode("utf-8")
            return f"data:{mime};base64,{b64}"
        except Exception:
            return ""

    @staticmethod
    def process_uploads(file_list) -> List[Dict]:
        if not file_list:
            return []
        if isinstance(file_list, str):
            file_list = [file_list]
        results = []
        for item in file_list:
            filepath = item if isinstance(item, str) else getattr(item, "name", str(item))
            if not filepath or not os.path.isfile(filepath):
                continue
            mime, _ = mimetypes.guess_type(filepath)
            if not mime or not mime.startswith("image/"):
                continue
            data_url = ImageProvider.file_to_data_url(filepath)
            if not data_url:
                continue
            size_kb = round(os.path.getsize(filepath) / 1024, 1)
            results.append({
                "name":     os.path.basename(filepath),
                "path":     filepath,
                "data_url": data_url,
                "size_kb":  size_kb,
            })
        return results


